def uni_lst(lst):
    ord_lst = []
    for i in lst:
        if i not in ord_lst:
            ord_lst.append(i)
    return ord_lst


print(uni_lst([1, 2, 3, 3, 3, 3, 4, 5, 1, 3, 5, 5, 5]))


