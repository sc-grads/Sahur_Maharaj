def perfect_no(numb):
    div = []
    for i in range(1, int(numb / 2) + 1):
        if numb % i == 0:
            div.append(i)
    if sum(div) == numb:
        return f'{numb} is a perfect number'
    else:
        return f'{numb} is not a perfect number'


print(perfect_no(496))
