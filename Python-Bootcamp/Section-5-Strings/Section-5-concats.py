# concat oprerator +
movie = 'Avenger'
fav_actor = 'Iron-man'
movie_and_actor = movie + fav_actor
print(movie_and_actor)  # <-- no space between words
print(movie + ' ' + fav_actor)  # <-- space

# number vars must be converted to str type
abc = 1
print(movie + str(abc))

# repetition *
print(movie * 5)
